    public String generaQR(String dato) {
        try {
            QRCode c = new QRCode();
            c.setData(dato);
            c.setDataMode(QRCode.MODE_BYTE);
            c.setUOM(udm);
            c.setLeftMargin(mi);
            c.setRightMargin(md);
            c.setTopMargin(ms);
            c.setBottomMargin(min);
            c.setResolution(resol);
            c.setRotate(rot);
            c.setModuleSize(tam);

            String archivo = System.getProperty("user.home") + "/nuevo.png";

            c.renderBarcode("qr.png");

            try {

                BufferedImage originalImage = ImageIO.read(new File("qr.png"));
                int type = originalImage.getType() == 0 ? BufferedImage.TYPE_INT_ARGB : originalImage.getType();

                BufferedImage resizeImageJpg = resizeImage(originalImage, type);
                ImageIO.write(resizeImageJpg, "png", new File("qr2.png"));

            } catch (IOException e) {
                System.out.println(e.getMessage());
            }
            //System.out.println(archivo);

            //Desktop d = Desktop.getDesktop();
            //d.open(new File(archivo));
            return archivo;

        } catch (Exception e) {
            System.out.println("Error" + e);
        }
        return "";

    }

    private BufferedImage resizeImage(BufferedImage originalImage, int type) {
        BufferedImage resizedImage = new BufferedImage(80, 80, type);
        Graphics2D g = resizedImage.createGraphics();
        g.drawImage(originalImage, 0, 0, 80, 80, null);
        g.dispose();

        return resizedImage;
    }

